
public class Do_While_Loops {
	public static void main(String[] args) {
		int g = 0;
		do {
			System.out.println(g);
			g++;
			} while (g <= 11); //difference between while and do while loops is 
								//While loops evaluates condition then executes command
								//Do While loops executes command first and then evaluates condition
	}
}
